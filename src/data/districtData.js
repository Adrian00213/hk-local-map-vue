// Get random district for discovery
export const getRandomDistrict = () => {
  const keys = Object.keys(DISTRICT_DATA)
  const randomKey = keys[Math.floor(Math.random() * keys.length)]
  return { key: randomKey, ...DISTRICT_DATA[randomKey] }
}

// Get today's recommended district (based on date)
export const getTodayDistrict = () => {
  const today = new Date()
  const dayOfYear = Math.floor((today - new Date(today.getFullYear(), 0, 0)) / 86400000)
  const keys = Object.keys(DISTRICT_DATA)
  const index = dayOfYear % keys.length
  const key = keys[index]
  return { key, ...DISTRICT_DATA[key] }
}
